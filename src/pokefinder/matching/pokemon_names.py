def expand_keyword(keyword: str) -> list[str]:
    """
    Return the keyword plus its translation (en→he or he→en).
    e.g. "pikachu" -> ["pikachu", "פיקאצ'ו"]
    e.g. "פיקאצ'ו" -> ["פיקאצ'ו", "pikachu"]
    """
    variants = [keyword]
    kw_lower = keyword.lower()
    if kw_lower in EN_TO_HE:
        variants.append(EN_TO_HE[kw_lower])
    if keyword in HE_TO_EN:
        variants.append(HE_TO_EN[keyword])
    return variants
